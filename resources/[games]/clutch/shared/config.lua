---@return ClutchMap[]
function Config.Clutch:getMaps()
    return self.maps or {}
end

---@param excludeName string | nil
---@return ClutchMap | nil
function Config.Clutch:pickRandomMap(excludeName)
    local maps = self:getMaps()
    if #maps == 0 then return nil end
    if #maps == 1 then return maps[1] end

    local pool = {}
    for i = 1, #maps do
        if maps[i].name ~= excludeName then
            pool[#pool + 1] = maps[i]
        end
    end
    if #pool == 0 then pool = maps end
    return pool[math.random(1, #pool)]
end

---@param variant string
---@return ClutchVariant | nil
function Config.Clutch:variantOf(variant)
    return self.variants[variant]
end

---@param variant string
---@return integer
function Config.Clutch:expectedPlayers(variant)
    local v = self:variantOf(variant)
    return v and v.totalPlayers or 0
end
