-- AGACHAR (crouch) — toggle no CTRL via keymapping
local crouched = false
local CROUCH_CLIPSET = "move_ped_crouched"
local CROUCH_STRAFE = "move_ped_crouched_strafing"

local function setCrouch(state)
	local ped = PlayerPedId()
	if state then
		RequestAnimSet(CROUCH_CLIPSET)
		RequestAnimSet(CROUCH_STRAFE)
		local deadline = GetGameTimer() + 1000
		while not HasAnimSetLoaded(CROUCH_CLIPSET) and GetGameTimer() < deadline do
			Wait(0)
		end
		if not HasAnimSetLoaded(CROUCH_CLIPSET) then
			return
		end
		SetPedMovementClipset(ped, CROUCH_CLIPSET, 0.25)
		if HasAnimSetLoaded(CROUCH_STRAFE) then
			SetPedStrafeClipset(ped, CROUCH_STRAFE)
		end
		crouched = true
	else
		ResetPedMovementClipset(ped, 0.25)
		ResetPedStrafeClipset(ped)
		crouched = false
	end
end

RegisterCommand("misc:crouch", function()
	local ped = PlayerPedId()
	if IsPedInAnyVehicle(ped, false) or IsEntityDead(ped) then
		return
	end
	setCrouch(not crouched)
end, false)

RegisterKeyMapping("misc:crouch", "Agachar", "keyboard", "LCONTROL")

CreateThread(function()
	while true do
		local wait = 1000
		local ped = PlayerPedId()
		if ped ~= 0 and not IsPedInAnyVehicle(ped, false) and not IsEntityDead(ped) then
			if crouched then
				wait = 0
				DisablePlayerFiring(PlayerId(), true)
			end
		end
		Wait(wait)
	end
end)

CreateThread(function()
	local lastCrouched = false
	while true do
		local ped = PlayerPedId()
		local inVeh = IsPedInAnyVehicle(ped, false)
		if not inVeh then
			DisableControlAction(0, 36, true)
		end

		if crouched then
			if inVeh or IsEntityDead(ped) then
				setCrouch(false)
			else
				if not HasAnimSetLoaded(CROUCH_CLIPSET) then
					RequestAnimSet(CROUCH_CLIPSET)
				else
					if lastCrouched ~= crouched then
						SetPedMovementClipset(ped, CROUCH_CLIPSET, 1.0)
					end
				end

				if not HasAnimSetLoaded(CROUCH_STRAFE) then
					RequestAnimSet(CROUCH_STRAFE)
				else
					if lastCrouched ~= crouched then
						SetPedStrafeClipset(ped, CROUCH_STRAFE)
					end
				end
			end
		end

		lastCrouched = crouched

		if crouched then
			Wait(10)
		else
			Wait(1000)
		end
	end
end)

